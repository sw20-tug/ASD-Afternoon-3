    public RoommateOverview(Flat flat) {
        this.activeFlat = flat;

        List<Roommate> roommates = new ArrayList<>();
        try {
            roommates = DatabaseConnection.getInstance().createDao(FlatDAO.class).getRoommates(flat);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(getContentPane(), "Roommates could not be found.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        tableModel = new RoommateTableModel(roommates);
        roommateTable.setModel(tableModel);

        addButton.addActionListener(e -> {
            Roommate roommate = createRoommate();
            if (roommate != null) {
                tableModel.addRoommate(roommate);
            }
        });

        removeButton.addActionListener(e -> {
            int rowIndex = roommateTable.getSelectedRow();
            Roommate roommate = tableModel.getRoommateAtIndex(rowIndex);

            try {
                RoommateDAO creator = DatabaseConnection.getInstance().createDao(RoommateDAO.class);
                creator.delete(roommate);

                tableModel.removeRoommate(rowIndex);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(contentPane, "Could not remove roommate");
            }
        });

        backButton.addActionListener(e -> {
            FlatApplication.get().setContentPane(new FlatOverview(activeFlat).getContentPane());
        });
    }

    private Roommate createRoommate() {
        Roommate newRoommate;
        int age = 0;

        try {
            age = Integer.parseInt(tfAge.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(getContentPane(), "Exception: Couldn't convert string to int: " + ex.getLocalizedMessage());
        }

        newRoommate = new Roommate(tfName.getText(), age, activeFlat);

        try {
            RoommateDAO creator = DatabaseConnection.getInstance().createDao(RoommateDAO.class);

            if (!creator.validate(newRoommate)) {
                JOptionPane.showMessageDialog(contentPane, "Roommate data is not valid");
            } else {
                return creator.create(newRoommate);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(contentPane, "Could not create roommate");
        }

        return null;
    }

